package de.bachelor.smartSchedules.server.model.xmlparser.exception;

public class OperationNotExistsException extends Exception{

	/**
	 * Seriennummer
	 */
	private static final long serialVersionUID = -5801218387447463953L;
	
	/**
	 * Default
	 */
	public OperationNotExistsException() {
		
	}
	
	/**
	 * Konstruktor
	 * @param operationName
	 */
	public OperationNotExistsException(String operationName) {
		super("Operation: " +operationName +" not exists!");
	}
	
	/**
	 * Konstruktor
	 * @param operationID
	 */
	public OperationNotExistsException(int operationID) {
		super("Operation: " +operationID +" not exists!");
	}
}
