package de.bachelor.smartSchedules.server.model.xmlparser.exception;
/**
 * Falls eine Variante nicht existiert.
 * @author timo
 *
 */
public class VariantNotExistsException extends Exception{

	/**
	 * Seriennummer
	 */
	private static final long serialVersionUID = 5818392757035718848L;
	
	/**
	 * Default
	 */
	public VariantNotExistsException() {
		
	}

	/**
	 * Konstruktor
	 * @param variantID
	 */
	public VariantNotExistsException(int variantID) {
		super("Variant: " +variantID +" not exists!");
	}
}
